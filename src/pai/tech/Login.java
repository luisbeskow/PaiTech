    public void logar()
    {
        
        String sql = "Select * from login where usuario = ? and senha = ? ";
        try
        {
            prepara = conecta.prepareStatement(sql);
            prepara.setString(1, login_campousuario.getText());
            prepara.setString(2, login_camposenha.getText());
            
            resulta=prepara.executeQuery();
            
            if (resulta.next())
            {
                tela_principal login_tela = new tela_principal();
                login_tela.setVisible(true);
                dispose();
                
            }
            
            else
            {
                JOptionPane.showMessageDialog(null, "Usuario ou senha inv√°lidos");
                
            }
        }
        
        catch(SQLException error)
                {
                JOptionPane.showMessageDialog(null, error);
           
                    
                }
        
        
        
        
    }
    
    
    
